package Q9.Letmecalculate;

public class Calculator {
    public int add(int a, int b){
        int c = a+b;
        return c;
    }
    
    public int sub(int a, int b) {
        int c = a-b;
        return c;
    }

    public int mul(int a, int b) {
        int c = a*b;
        return c;
    }

    public float div(int a, int b) {
        float c = a/b;
        return c;
    }
}
